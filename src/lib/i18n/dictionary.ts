function normalizedSemanticValue(value: string | null | undefined) {
  return value?.replace(/[_-]+/g, " ").trim().toLowerCase() ?? "";
}

function semanticLabel(language: Language, value: string | null | undefined, keys: Record<string, string>) {
  const raw = value?.trim();
  if (!raw) return "-";
  const key = keys[normalizedSemanticValue(raw)];
  return key ? translate(language, key) : raw;
}

export function roleLabel(language: Language, role: Role) {
  return translate(language, roleKeys[role]);
}

export function requisitionStatusLabel(language: Language, status: RequisitionStatus | string | null | undefined) {
  if (status === "ongoing") return translate(language, "statusOngoing");
  if (status === "filled") return translate(language, "statusFilled");
  if (status === "cancel") return translate(language, "statusCancel");
  return status ?? "-";
}

export function requestTypeLabel(language: Language, value: RequisitionRequestType | string | null | undefined) {
  if (value === "New") return translate(language, "requestNew");
  if (value === "Replacement") return translate(language, "requestReplacement");
  return value ?? "-";
}

export function processStageLabel(language: Language, stage: ProcessStage | "Resume Screening" | "No activity" | null | undefined) {
  if (!stage || stage === "No activity") return translate(language, "noActivity");
  if (stage === "Resume Screening") return translate(language, "processResumeScreening");
  return translate(language, processKeys[stage] ?? stage);
}

export function resultLabel(language: Language, result: ResultValue) {
  if (result === 1) return translate(language, "resultPass");
  if (result === 0) return translate(language, "resultFail");
  return translate(language, "resultPending");
}

